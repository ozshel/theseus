     * @var strings
     *
     * @Assert\File( maxSize="2M", maxSizeMessage = "Votre image est trop volumineuse (2 Mo max).", mimeTypes = {"image/png"}, mimeTypesMessage = "Les formats acceptés sont : png, jpeg" )
